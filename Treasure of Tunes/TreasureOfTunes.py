can_continue = True
print("Welcome to Muse Island!\nYour mission is to find the lost melody.")
while can_continue:
    guitar_or_drum = input("\nChoose your instrument:\n\tType 'guitar' or 'drum'\n").lower()
    if guitar_or_drum == "guitar":
        print("You picked up the guitar and played a moving intro, drawing the admiration of the local residents.")
        break
    elif guitar_or_drum == "drum":
        print("You tried striking the drum, but completely missed the mark, angering the islanders present.\nYou have been expelled from Muse Island.\nGame Over.")
        continue_check = input("Do you wanna try again? (Press 'Y' to continue) ").lower()
        if continue_check != "y":
            can_continue = False
            break
    else:
        print("Oops, invalid input. Please try again!")

